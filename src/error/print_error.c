#include "minishell.h"

void	print_error(char *str, char *word)
{
	write(STDERR_FILENO, str, ft_strlen(str));
	write(STDERR_FILENO, word, ft_strlen(word));
	write(STDERR_FILENO, "\n", 1);
}
